    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cellCidade", for: indexPath) as! CidadeTableViewCell

        cell.nomeLabel.text = cidades[indexPath.row].nome! + "/" + cidades[indexPath.row].estado!
        
        cell.informacoesLabel.text = cidades[indexPath.row].info!
        
        if let imagem = cidades[indexPath.row].foto {
            cell.fotoImageView.image = UIImage(named: imagem)
        }
        
        

        return cell
    }
 

